let lang = 'en';

function langChanger() {
  const langElement = document.querySelector('.lang');
  const langBtn = document.querySelector('.langChanger button');

  lang = lang == 'en' ? 'pl' : 'en';

  document.documentElement.lang = lang;
  langElement.innerText = lang;
  if (langBtn) langBtn.classList.toggle('active');

  document.querySelectorAll('[data-text]').forEach((item) => {
    const text = item.getAttribute('data-text');
    item.classList.add('hidden');
    setTimeout(() => {
      item.innerHTML = language[text][lang];
      item.classList.remove('hidden');
    }, 300);
  });

  document.querySelectorAll('[data-placeholder]').forEach((item) => {
    const text = item.getAttribute('data-placeholder');
    item.classList.add('hidden');
    setTimeout(() => {
      item.setAttribute('placeholder', language[text][lang]);
      item.classList.remove('hidden');
    }, 300);
  });
}
